class colorClass:
    def PURPLE(x):
        return "\033[95m"

    def CYAN(x):
        return "\033[96m"

    def DARKCYAN(x):
        return "\033[36m"

    def RED(x):
        return "\033[91m"

    def BLUE(x):
        return "\033[94m"

    def GREEN(x):
        return "\033[92m"

    def YELLOW(x):
        return "\033[93m"

    def BOLD(x):
        return "\033[1m"

    def UNDERLINE(x):
        return "\033[4m"

    def END(x):
        return "\033[0m"
